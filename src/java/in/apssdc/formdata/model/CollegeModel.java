package in.apssdc.formdata.model;

public class CollegeModel {
	
	private String collegeName;
	private Integer yearOfEstd;
	private String principalName;
	private Long mobileNumber;
	private String emailId;
	private String website;
	private String socialNetworkingLink;
	
	public String getCollegeName() {
		return collegeName;
	}
	public void setCollegeName(String collegeName) {
		this.collegeName = collegeName;
	}
	public Integer getYearOfEstd() {
		return yearOfEstd;
	}
	public void setYearOfEstd(Integer yearOfEstd) {
		this.yearOfEstd = yearOfEstd;
	}
	public String getPrincipalName() {
		return principalName;
	}
	public void setPrincipalName(String principalName) {
		this.principalName = principalName;
	}
	public Long getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(Long mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getWebsite() {
		return website;
	}
	public void setWebsite(String website) {
		this.website = website;
	}
	public String getSocialNetworkingLink() {
		return socialNetworkingLink;
	}
	public void setSocialNetworkingLink(String socialNetworkingLink) {
		this.socialNetworkingLink = socialNetworkingLink;
	}
	
	

}
